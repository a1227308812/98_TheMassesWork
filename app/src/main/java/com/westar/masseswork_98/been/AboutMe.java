public class AboutMe {
    String logoUrl;
    String appName;
    String appDescrible;
    String telNum;
    String email;
    String webSite;

    public String getLogoUrl() {
        return logoUrl;
    }

    public AboutMe setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
        return this;
    }

    public String getAppName() {
        return appName;
    }

    public AboutMe setAppName(String appName) {
        this.appName = appName;
        return this;
    }

    public String getAppDescrible() {
        return appDescrible;
    }

    public AboutMe setAppDescrible(String appDescrible) {
        this.appDescrible = appDescrible;
        return this;
    }

    public String getTelNum() {
        return telNum;
    }

    public AboutMe setTelNum(String telNum) {
        this.telNum = telNum;
        return this;
    }

    public String getEmail() {
        return email;
    }

    public AboutMe setEmail(String email) {
        this.email = email;
        return this;
    }

    public String getWebSite() {
        return webSite;
    }

    public AboutMe setWebSite(String webSite) {
        this.webSite = webSite;
        return this;
    }
}
